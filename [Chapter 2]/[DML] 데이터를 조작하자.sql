/*************************************************************************************/

--PRIMARY KEY 제약 조건 위반
INSERT INTO  [회원테이블]  VALUES ('A10001', '홍길동', '남', 29, 300000, '2020-01-03', NULL); --PK키(회원번호)가 중복되어서는 안 됨

--NOT NULL 제약 조건 위반
INSERT INTO  [회원테이블]  VALUES ('A10005', '홍길동', '남', 29, 300000, NULL, 1);   -- 가입일자 컬럼에 NULL값이 있어서는 안 됨 (NOT NULL)

/*************************************************************************************/

--[회원테이블]에 모든 컬럼 조회
SELECT  * --모든 컬럼
  FROM  [회원테이블]

--[회원테이블]에 특정 컬럼명 조회 및 임시로 컬럼명 변경하기
SELECT  [회원번호]
		,[이름] AS [성명]   -- AS(Aliases) : 별칭 생성
		,[가입일자]
		,[나이]
  FROM  [회원테이블]

/*************************************************************************************/

--모든 행을 조건 없이, [나이] 30 으로 수정하기 
UPDATE  [회원테이블]
   SET  [나이] = 30    -- 변경할 열 및 값을 설정

--[회원테이블] 조회 및 수정 확인
SELECT  * --모든 컬럼
  FROM  [회원테이블]

/*************************************************************************************/

--[회원번호]가 'A10001'인 [나이] 34 로 변경하기 
UPDATE  [회원테이블]
   SET  [나이] = 34
 WHERE  [회원번호] = 'A10001'    -- WHERE 조건절를 활용하여 특정 조건으로 필터링

--[회원테이블] 조회 및 변경 확인
SELECT  * --모든 컬럼
  FROM  [회원테이블]

/*************************************************************************************/


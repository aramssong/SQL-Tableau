USE EDU --[EDU] 데이터베이스를 사용합니다.

--[회원테이블] 생성
CREATE TABLE [회원테이블] (
[회원번호] VARCHAR(20) PRIMARY KEY,     -- 20만큼의 최대 길이 가짐 / 주요 키
[이름] VARCHAR(10),     -- 10만큼의 최대 길이 가짐
[성별] VARCHAR(2),      -- 2만큼의 최대 길이 가짐
[나이] INT,             -- 정수형
[가입금액] MONEY,       -- 화폐 단위
[가입일자] DATE NOT NULL,     -- 날짜 / NULL 값을 가지면 안 됨
[수신동의] BIT          -- 참 또는 거짓
)

/*************************************************************************************/

--[회원테이블]에 데이터 삽입 (행 단위)
-- 문자/날짜형 : 작은 따옴표 추가
-- 열 구분 : 쉼표 필요
